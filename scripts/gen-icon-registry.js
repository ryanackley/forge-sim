#!/usr/bin/env node
/**
 * Generates renderer/src/icon-registry.ts from installed @atlaskit/icon package.
 * Run after upgrading @atlaskit/icon: node scripts/gen-icon-registry.js
 */
const fs = require('fs');
const path = require('path');

const iconDir = path.join(__dirname, '..', 'renderer', 'node_modules', '@atlaskit', 'icon', 'core');
const outFile = path.join(__dirname, '..', 'renderer', 'src', 'icon-registry.ts');

const icons = fs.readdirSync(iconDir)
  .filter(f => f.endsWith('.js') && !f.endsWith('.d.js'))
  .map(f => f.replace('.js', ''))
  .sort();

const varName = n => 'Icon_' + n.replace(/-/g, '_');

const code = `// AUTO-GENERATED by scripts/gen-icon-registry.js — do not edit manually.
// Regenerate after upgrading @atlaskit/icon:  node scripts/gen-icon-registry.js
import type { ComponentType } from 'react';
${icons.map(n => `import ${varName(n)} from '@atlaskit/icon/core/${n}';`).join('\n')}

export const iconRegistry: Record<string, ComponentType<any>> = {
${icons.map(n => `  '${n}': ${varName(n)},`).join('\n')}
};
`;

fs.writeFileSync(outFile, code);
console.log(`Generated ${outFile} with ${icons.length} icons.`);
