// ── UI Module Detection ──────────────────────────────────────────────────

export interface DetectedModule {
  module: ManifestUIModule;
  resourcePath: string;
  mode: 'uikit' | 'customui';
}

/**
 * Detect whether a UI module uses UIKit or Custom UI.
 *
 * UIKit modules:
 *   - Have a resource that points to a file importing @forge/react
 *   - The resource file calls ForgeReconciler.render()
 *
 * Custom UI modules:
 *   - Have a resource that points to a directory with index.html
 *   - Or a file that doesn't import @forge/react
 */
export function detectModuleType(appDir: string, manifest: ParsedManifest, mod: ManifestUIModule): DetectedModule | null {
  if (!mod.resourceKey) {
    // No resource = server-only module (no UI to render)
    return null;
  }

  const resource = manifest.resources.get(mod.resourceKey);
  if (!resource) return null;

  const resourcePath = resolve(appDir, resource.path);

  // Check if it's a directory with index.html (Custom UI)
  if (existsSync(join(resourcePath, 'index.html'))) {
    return { module: mod, resourcePath, mode: 'customui' };
  }

  // Check if the resource points to a file
  const possibleFiles = [
    resourcePath,
    resourcePath + '.tsx',
    resourcePath + '.ts',
    resourcePath + '.jsx',
    resourcePath + '.js',
    join(resourcePath, 'index.tsx'),
    join(resourcePath, 'index.ts'),
    join(resourcePath, 'index.jsx'),
    join(resourcePath, 'index.js'),
  ];

  for (const filePath of possibleFiles) {
    if (existsSync(filePath)) {
      // Read file to check for @forge/react imports (UIKit indicator)
      try {
        const content = readFileSync(filePath, 'utf-8');
        if (content.includes('@forge/react') || content.includes('ForgeReconciler')) {
          return { module: mod, resourcePath: filePath, mode: 'uikit' };
        }
      } catch {}

      // If no @forge/react import, assume Custom UI
      return { module: mod, resourcePath: filePath, mode: 'customui' };
    }
  }

  return null;
}

// ── Entry Point Generation ────────────────────────────────────────────────

function generateUIKitEntry(appResourcePath: string, wsPort: number, strictMode: boolean): string {
  const shellElement = 'React.createElement(ForgeSimShell)';
  const rootElement = strictMode
    ? `React.createElement(React.StrictMode, null, ${shellElement})`
    : shellElement;

  return `
/**
 * Auto-generated by forge-sim dev
 * DO NOT EDIT — this file is regenerated on every run
 */

// Use dynamic imports to guarantee execution order:
// 1. Bridge shim installs globalThis.__bridge
// 2. Dev's app code runs (calls ForgeReconciler.render → produces ForgeDoc)
// 3. React DOM shell mounts (picks up ForgeDoc via onReconcile listener)

async function boot() {
  // 1. Install bridge shim — must happen before any @forge/react code
  const bridge = await import('forge-sim/renderer/bridge');
  bridge.configure({ wsUrl: 'ws://localhost:${wsPort}' });

  // 2. Import the developer's app — triggers ForgeReconciler.render()
  //    The bridge shim buffers the ForgeDoc so step 3 can pick it up
  await import('${appResourcePath}');

  // 3. Mount the Atlaskit renderer shell
  const React = await import('react');
  const ReactDOM = await import('react-dom/client');
  const { ForgeSimShell } = await import('forge-sim/renderer/ForgeSimShell');

  ReactDOM.createRoot(document.getElementById('root')!).render(
    ${rootElement}
  );
}

boot().catch((err) => {
  console.error('[forge-sim] Boot failed:', err);
  document.getElementById('root')!.innerHTML =
    '<div style="padding:40px;font-family:monospace;color:#de350b">' +
    '<h2>⚠️ forge-sim failed to start</h2>' +
    '<pre>' + (err.stack || err.message || err) + '</pre></div>';
});
`;
}

/**
 * Generate a self-contained inline script that installs window.__bridge
 * for pre-built Custom UI apps. This runs before any bundled app code,
 * so @forge/bridge's getCallBridge() finds our bridge immediately.
 */
function generateBridgeInlineScript(wsPort: number): string {
  return `
(function() {
  var S = {
    ws: null, wsReady: false, wsUrl: 'ws://localhost:${wsPort}',
    pendingRequests: {}, requestCounter: 0, reconcileListeners: [], lastForgeDoc: null
  };
  window.__forgeSim = S;

  function ensureConnection() {
    if (S.ws && S.wsReady) return Promise.resolve();
    return new Promise(function(resolve, reject) {
      if (S.ws && S.ws.readyState === WebSocket.CONNECTING) {
        S.ws.addEventListener('open', function() { resolve(); }, { once: true });
        return;
      }
      S.ws = new WebSocket(S.wsUrl);
      S.ws.onopen = function() { S.wsReady = true; resolve(); };
      S.ws.onmessage = function(event) {
        try {
          var msg = JSON.parse(event.data);
          if (msg.requestId && S.pendingRequests[msg.requestId]) {
            var pending = S.pendingRequests[msg.requestId];
            delete S.pendingRequests[msg.requestId];
            if (msg.error) pending.reject(new Error(msg.error));
            else pending.resolve(msg.result);
          }
        } catch(e) {}
      };
      S.ws.onclose = function() {
        S.wsReady = false;
        Object.keys(S.pendingRequests).forEach(function(id) {
          S.pendingRequests[id].reject(new Error('WebSocket disconnected'));
          delete S.pendingRequests[id];
        });
        setTimeout(function() { ensureConnection(); }, 2000);
      };
      S.ws.onerror = function() { S.wsReady = false; reject(new Error('WebSocket connection failed')); };
    });
  }

  function rpc(method, params) {
    return ensureConnection().then(function() {
      var requestId = 'rpc-' + (++S.requestCounter) + '-' + Date.now();
      return new Promise(function(resolve, reject) {
        var timeout = setTimeout(function() {
          delete S.pendingRequests[requestId];
          reject(new Error('RPC timeout: ' + method));
        }, 30000);
        S.pendingRequests[requestId] = {
          resolve: function(v) { clearTimeout(timeout); resolve(v); },
          reject: function(e) { clearTimeout(timeout); reject(e); }
        };
        S.ws.send(JSON.stringify({ type: 'rpc', requestId: requestId, method: method, params: params || {} }));
      });
    });
  }

  function getModuleKeyFromURL() {
    var match = window.location.pathname.match(/\\/module\\/([^/]+)/);
    return match ? match[1] : undefined;
  }

  function callBridge(cmd, data) {
    switch(cmd) {
      case 'reconcile':
        if (data && data.forgeDoc) {
          S.lastForgeDoc = data.forgeDoc;
          S.reconcileListeners.forEach(function(l) { try { l(data.forgeDoc); } catch(e) {} });
        }
        return Promise.resolve();
      case 'invoke':
        if (data && data.invokeType && data.invokeType.startsWith('ui-') && data.invokeType.endsWith('-fetch')) {
          return rpc('invokeRemote', {
            path: data.path,
            method: data.method,
            headers: data.headers,
            body: data.body,
            moduleKey: getModuleKeyFromURL()
          });
        }
        return rpc('invoke', { functionKey: data && data.functionKey, payload: data && data.payload, moduleKey: getModuleKeyFromURL() });
      case 'fetchProduct':
        return rpc('fetchProduct', {
          product: data && data.product, restPath: data && data.restPath,
          fetchRequestInit: data && data.fetchRequestInit
        });
      case 'fetchRemote':
        return rpc('fetchRemote', {
          remoteKey: data && data.remoteKey,
          fetchRequestInit: data && data.fetchRequestInit
        });
      case 'getContext':
        return rpc('getContext', { moduleKey: getModuleKeyFromURL() });
      case 'submit':
        return rpc('viewSubmit', { payload: data && data.payload || data });
      case 'close':
        return rpc('viewClose', { payload: data && data.payload || data });
      case 'refresh':
        window.location.reload();
        return Promise.resolve();
      case 'enableTheming':
        return Promise.resolve();
      default:
        console.warn('[forge-sim bridge] Unhandled command: ' + cmd);
        return Promise.resolve();
    }
  }

  window.__bridge = { callBridge: callBridge };

  // Pre-connect WebSocket so it's ready when the app loads
  ensureConnection();
})();
`;
}

