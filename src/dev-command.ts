// ── Multi-Module Vite Middleware ───────────────────────────────────────────

/**
 * Installs Vite middleware that:
 * - `/` → module picker page
 * - `/module/<key>/` → serves that module's entry
 * - Everything else passes through to Vite (including __tools)
 */
export function installModuleRouting(
  viteServer: any,
  modules: DetectedModule[],
  tempDir: string,
): void {
  const modulesByKey = new Map(modules.map((m) => [m.module.key, m]));

  const middleware = (req: any, res: any, next: any) => {
    const url = new URL(req.url ?? '/', 'http://localhost');

    // Root → module picker (but only for exact '/' with no file extension)
    if (url.pathname === '/') {
      res.writeHead(200, { 'Content-Type': 'text/html', 'Cache-Control': 'no-cache' });
      res.end(generateModulePickerHtml(modules));
      return;
    }

    // /module/<key>/ → rewrite to serve that module's index.html via Vite
    const match = url.pathname.match(/^\/module\/([^/]+)(\/.*)?$/);
    if (match) {
      const key = match[1];
      const rest = match[2] || '/';
      const mod = modulesByKey.get(key);

      if (!mod) {
        res.writeHead(404, { 'Content-Type': 'text/html' });
        res.end(`<h1>404</h1><p>Module "${key}" not found.</p><p><a href="/">Back to module picker</a></p>`);
        return;
      }

      if (mod.mode === 'uikit') {
        // Rewrite URL to serve from the module's temp subdirectory
        // /module/<key>/ → /<key>/index.html
        // /module/<key>/entry.tsx → /<key>/entry.tsx
        if (rest === '/' || rest === '/index.html') {
          req.url = `/${key}/index.html`;
        } else {
          req.url = `/${key}${rest}`;
        }
      } else {
        // Custom UI — rewrite to serve from /_customui_<key>/ virtual prefix
        // The Vite alias handles mapping this to the actual resource directory
        if (rest === '/' || rest === '/index.html') {
          req.url = `/_customui_${key}/index.html`;
        } else {
          req.url = `/_customui_${key}${rest}`;
        }
      }
    }

    next();
  };

  // Prepend to Vite's middleware stack (before Vite's catch-all)
  const stack = (viteServer.middlewares as any).stack;
  if (Array.isArray(stack)) {
    stack.unshift({ route: '', handle: middleware });
  } else {
    viteServer.middlewares.use(middleware);
  }
}

// ── Vite Config Builder ───────────────────────────────────────────────────

/**
 * Build the Vite config as a JS object — passed directly to createServer().
 * No config file needed, so Vite doesn't have to resolve 'vite' or plugin
 * imports from the app's directory.
 *
 * Multi-module mode: Vite root is the temp dir containing per-module
 * subdirectories (<key>/entry.tsx + index.html for UIKit modules).
 * Custom UI modules are served via aliases mapping /_customui_<key>/ paths
 * to their resource directories.
 */
async function buildViteConfig(opts: {
  appDir: string;
  tempDir: string;
  modules: DetectedModule[];
  wsPort: number;
  port: number;
  forgeSimRoot: string;
}): Promise<any> {
  const { appDir, tempDir, modules, wsPort, port, forgeSimRoot } = opts;
  const shimPath = join(forgeSimRoot, 'renderer', 'src', 'bridge', 'forge-bridge-shim.ts');
  const bridgeBarrel = join(forgeSimRoot, 'renderer', 'src', 'bridge', 'index.ts');

  const rendererNodeModules = join(forgeSimRoot, 'renderer', 'node_modules');
  const mainNodeModules = join(forgeSimRoot, 'node_modules');
  const appNodeModules = join(appDir, 'node_modules');

  // Import the plugin directly — it's in forge-sim's node_modules
  const reactPlugin = (await import('@vitejs/plugin-react')).default;

  // Build alias map: standard aliases + Custom UI resource directories
  const aliases: Record<string, string> = {
    '@forge/bridge': shimPath,
    'forge-sim/renderer/bridge': bridgeBarrel,
    'forge-sim/renderer/bridge-shim': shimPath,
    'forge-sim/renderer/ForgeDocRenderer': join(forgeSimRoot, 'renderer', 'src', 'ForgeDocRenderer.tsx'),
    'forge-sim/renderer/ForgeSimShell': join(forgeSimRoot, 'renderer', 'src', 'ForgeSimShell.tsx'),
  };

  // Collect all directories Vite needs fs access to
  const fsAllow = [forgeSimRoot, appDir, tempDir];

  // For Custom UI modules, add a Vite plugin to resolve /_customui_<key>/ paths
  const customUiModules = modules.filter((m) => m.mode === 'customui');
  for (const m of customUiModules) {
    fsAllow.push(m.resourcePath);
  }

  // Custom UI rewrite plugin — maps /_customui_<key>/path to the resource directory
  const customUiPlugin = {
    name: 'forge-sim-custom-ui',
    configureServer(server: any) {
      server.middlewares.use((req: any, _res: any, next: any) => {
        if (!req.url) return next();
        const match = req.url.match(/^\/_customui_([^/]+)(\/.*)?$/);
        if (!match) return next();
        const key = match[1];
        const rest = match[2] || '/index.html';
        const mod = modules.find((m) => m.module.key === key && m.mode === 'customui');
        if (!mod) return next();
        // Rewrite to serve from the resource directory
        // For directory-based Custom UI (with index.html), resourcePath is the dir
        const resourceDir = existsSync(join(mod.resourcePath, 'index.html'))
          ? mod.resourcePath
          : dirname(mod.resourcePath);
        req.url = rest;
        // Use Vite's static file serving by changing the url
        // We need to actually serve the file from the right directory
        const filePath = join(resourceDir, rest);
        if (existsSync(filePath)) {
          const ext = filePath.split('.').pop() || '';
          const mimeTypes: Record<string, string> = {
            html: 'text/html', js: 'application/javascript', ts: 'application/javascript',
            tsx: 'application/javascript', jsx: 'application/javascript',
            css: 'text/css', json: 'application/json', svg: 'image/svg+xml',
            png: 'image/png', jpg: 'image/jpeg', ico: 'image/x-icon',
          };

          // For HTML files, inject the bridge shim before any app scripts
          if (ext === 'html') {
            let html = readFileSync(filePath, 'utf-8');
            const bridgeScript = generateBridgeInlineScript(wsPort);
            // Inject right after <head> (before any other scripts)
            html = html.replace(/<head([^>]*)>/i, `<head$1>\n<script>${bridgeScript}</script>`);
            _res.writeHead(200, { 'Content-Type': 'text/html' });
            _res.end(html);
            return;
          }

          const content = readFileSync(filePath);
          _res.writeHead(200, { 'Content-Type': mimeTypes[ext] || 'application/octet-stream' });
          _res.end(content);
          return;
        }
        next();
      });
    },
  };

  return {
    configFile: false as const,
    root: tempDir,
    plugins: [reactPlugin(), customUiPlugin],
    server: {
      port,
      host: true,
      open: false,
      fs: {
        allow: [...new Set(fsAllow)],
      },
    },
    resolve: {
      alias: aliases,
      modules: [
        rendererNodeModules,
        appNodeModules,
        mainNodeModules,
        'node_modules',
      ],
    },
    define: {
      '__FORGE_SIM_WS_URL__': JSON.stringify(`ws://localhost:${wsPort}`),
      // @atlaskit/editor-core and @atlaskit/renderer deep deps reference
      // process.env (Node global) which doesn't exist in the browser.
      'process.env.NODE_ENV': JSON.stringify('development'),
      'process.env': JSON.stringify({}),
    },
    optimizeDeps: {
      include: ['react', 'react-dom', 'react/jsx-runtime'],
    },
  };
}

// ── Main Dev Command ──────────────────────────────────────────────────────

export async function devCommand(options: DevCommandOptions) {
  const { appDir, port, wsPort, open, moduleKey, clean, strictMode } = options;
  const stateDir = join(appDir, '.forge-sim', 'state');

  console.log('');
  console.log('  🔥 forge-sim dev');
  console.log('  ─────────────────');
  console.log('');

  // 1. Validate app directory
  const manifestPath = join(appDir, 'manifest.yml');
  if (!existsSync(manifestPath)) {
    console.error(`  ❌ No manifest.yml found in ${appDir}`);
    console.error(`     Are you in a Forge app directory?`);
    process.exit(1);
  }

  // 2. Parse manifest
  console.log(`  📋 Reading manifest.yml...`);
  const manifest = await parseManifest(manifestPath);
  console.log(`     App: ${manifest.raw.app?.name ?? manifest.raw.app?.id ?? 'unnamed'}`);
  console.log(`     Functions: ${manifest.functions.size}`);
  console.log(`     UI modules: ${manifest.uiModules.length}`);
  console.log('');

  // 3. Find UI module to render
  const detectedModules: DetectedModule[] = [];
  for (const mod of manifest.uiModules) {
    if (moduleKey && mod.key !== moduleKey) continue;
    const detected = detectModuleType(appDir, manifest, mod);
    if (detected) detectedModules.push(detected);
  }

  if (detectedModules.length === 0) {
    console.error(`  ❌ No UI modules found${moduleKey ? ` with key "${moduleKey}"` : ''}`);
    console.error(`     Your manifest needs a module with a resource (jira:issuePanel, etc.)`);
    process.exit(1);
  }

  // In multi-module mode, we serve ALL modules. The --module flag
  // determines the default redirect from / when there's only one.
  const primaryModule = moduleKey
    ? detectedModules.find((d) => d.module.key === moduleKey) ?? detectedModules[0]
    : detectedModules[0];

  for (const mod of detectedModules) {
    const modeLabel = mod.mode === 'uikit' ? 'UIKit 2' : 'Custom UI';
    console.log(`  🎯 Module: ${mod.module.key} (${mod.module.type}) — ${modeLabel}`);
    console.log(`     Resource: ${relative(appDir, mod.resourcePath)}`);
  }
  console.log('');

  // 4. Create simulator and deploy resolvers
  console.log(`  ⚙️  Starting simulator...`);
  const sim = new ForgeSimulator();

  // 4a. Configure SQL restore BEFORE deploy — deploy can trigger scheduled
  // triggers (migrations) which lazily start SQL. The init file must be set
  // before that happens so persisted tables exist when migrations check.
  if (!clean) {
    const sqlDumpPath = await getSQLDumpPath(stateDir);
    if (sqlDumpPath) {
      sim.sql.setInitSQLFilePath(sqlDumpPath);
      // Read table count for logging
      const { readFileSync } = await import('node:fs');
      const dump = readFileSync(sqlDumpPath, 'utf-8');
      const tableCount = (dump.match(/CREATE TABLE/gi) || []).length;
      console.log(`  📂 Restoring SQL state (${tableCount} tables) on MySQL boot`);
    }
  }

  // Set the global simulator instance so that @forge/* shims
  // (loaded via our module hooks) can register handlers directly
  const { setSimulator } = await import('./shims/globals.js');
  setSimulator(sim);

  // Deploy functions (resolvers, triggers, etc.) — these run on the server side
  // For browser mode, we skip loading UI resources (they'll run in the browser)
  try {
    console.log(`     Resolver before deploy: [${[...sim.resolver.getHandlerMap().keys()].join(', ')}]`);
    const deployResult = await deployResolversOnly(sim, appDir, manifest);
    console.log(`     Resolver after deploy: [${[...sim.resolver.getHandlerMap().keys()].join(', ')}]`);
    if (deployResult.loadedFunctions.length > 0) {
      console.log(`     Loaded ${deployResult.loadedFunctions.length} function(s): ${deployResult.loadedFunctions.join(', ')}`);
    }
    if (deployResult.errors.length > 0) {
      for (const err of deployResult.errors) {
        console.warn(`     ⚠️  ${err.functionKey}: ${err.error}`);
      }
    }
  } catch (err: any) {
    console.warn(`     ⚠️  Deploy warning: ${err.message}`);
  }

  // 4b. Restore KVS state (unless --clean)
  // (SQL restore already configured in 4a via initSQLFilePath)
  if (!clean) {
    if (await hasPersistedState(stateDir)) {
      await loadState(sim, stateDir);
    }
  }

  // 4c. Connect to real Atlassian APIs if credentials exist
  try {
    const { loadCredentials, getDefaultAccount, saveCredentials, upsertAccount } = await import('./auth/credentials.js');
    const store = await loadCredentials(appDir);
    const account = getDefaultAccount(store);

    if (account) {
      // Set up OAuth config for token refresh (only needed for OAuth accounts)
      if (account.authType === 'oauth') {
        try {
          const { getOAuthAppConfig } = await import('./auth/config.js');
          const { setOAuthConfig } = await import('./auth/oauth.js');
          const oauthAppConfig = await getOAuthAppConfig();
          if (oauthAppConfig) setOAuthConfig(oauthAppConfig);
        } catch {}
      }

      sim.productApi.connectRealApis(account, {
        onTokenRefresh: (refreshedAccount) => {
          upsertAccount(store, refreshedAccount);
          saveCredentials(store).catch(() => {});
        },
      });
    } else {
      console.log(`  📡 No Atlassian accounts — using mock APIs`);
      console.log(`     Run 'forge-sim auth' to connect to a real site`);
    }
  } catch (err: any) {
    console.log(`  📡 Using mock APIs (auth load failed: ${err.message})`);
  }

  console.log('');

  // 5. Start WebSocket dev server (for RPC: invoke, fetchProduct, etc.)
  console.log(`  🔌 Starting WebSocket bridge on port ${wsPort}...`);
  const devServer = createDevServer({
    port: wsPort,
    watchDir: appDir,
    simulator: sim,
    context: buildDefaultContext(
      primaryModule.module.key,
      primaryModule.module.type,
      sim.productApi.connectedAccount,
    ),
  });

  // 6. Generate temp project for Vite (multi-module)
  const forgeSimRoot = resolve(__dirname, '..');
  const tempDir = join(appDir, '.forge-sim', 'tmp');

  // Clean up previous temp files (but preserve .forge-sim/state/)
  if (existsSync(tempDir)) {
    rmSync(tempDir, { recursive: true, force: true });
  }
  mkdirSync(tempDir, { recursive: true });
  writeFileSync(join(appDir, '.forge-sim', '.gitignore'), '*\n');

  // Generate per-module entry points
  for (const mod of detectedModules) {
    if (mod.mode === 'uikit') {
      const moduleDir = join(tempDir, mod.module.key);
      mkdirSync(moduleDir, { recursive: true });

      const appResourceRelative = relative(moduleDir, mod.resourcePath).replace(/\\/g, '/');
      const appResourceImportPath = appResourceRelative.startsWith('.') ? appResourceRelative : './' + appResourceRelative;
      const entryContent = generateUIKitEntry(appResourceImportPath, wsPort, !!strictMode);

      writeFileSync(join(moduleDir, 'entry.tsx'), entryContent);
      writeFileSync(join(moduleDir, 'index.html'), generateIndexHtml(
        `${mod.module.key} — ${manifest.raw.app?.name ?? 'Forge App'}`
      ));
    }
    // Custom UI modules don't need temp files — served from resource dir via middleware
  }

  // Write a minimal root index.html (Vite needs one at root)
  writeFileSync(join(tempDir, 'index.html'), generateIndexHtml(manifest.raw.app?.name ?? 'Forge App'));

  const uikitCount = detectedModules.filter((m) => m.mode === 'uikit').length;
  const customUiCount = detectedModules.filter((m) => m.mode === 'customui').length;
  console.log(`  📦 Generated multi-module dev environment (${uikitCount} UIKit, ${customUiCount} Custom UI)`);
  console.log('');

  // 7. Start Vite
  console.log(`  🚀 Starting Vite dev server...`);
  console.log('');

  try {
    const { createServer } = await import('vite');

    const viteConfig = await buildViteConfig({
      appDir,
      tempDir,
      modules: detectedModules,
      wsPort,
      port,
      forgeSimRoot,
    });

    const viteServer = await createServer(viteConfig);

    // Install multi-module routing middleware (before Vite's catch-all)
    installModuleRouting(viteServer, detectedModules, tempDir);

    await viteServer.listen();

    const info = viteServer.resolvedUrls;
    const localUrl = info?.local?.[0] ?? `http://localhost:${port}`;
    const networkUrl = info?.network?.[0];

    console.log('  ─────────────────────────────────────────────');
    console.log('');
    console.log(`  🔥 forge-sim dev server running!`);
    console.log('');
    console.log(`     ${detectedModules.length} module${detectedModules.length !== 1 ? 's' : ''} available`);
    console.log('');
    console.log(`     ➜ Local:   ${localUrl}`);
    if (networkUrl) {
      console.log(`     ➜ Network: ${networkUrl}`);
    }
    console.log(`     ➜ WS:      ws://localhost:${wsPort}`);
    if (manifest.remotes.size > 0) {
      console.log(`     ➜ JWKS:    ${localUrl}/__forge/jwks.json`);
    }

    // 8. Start Forge Sim Tools (uses Vite's HTTP server via middleware)
    const { attachToolsToVite } = await import('./tools/server.js');
    const toolsServer = attachToolsToVite({ sim, manifest, viteServer, appDir });

    // Hook simulator logs into tools broadcast
    sim.onLog((entry: any) => {
      toolsServer.broadcastLog(entry);
    });

    // 8b. Serve FIT JWKS endpoint at /__forge/jwks.json
    const forgeMiddleware = (req: any, res: any, next: any) => {
      const url = new URL(req.url ?? '/', 'http://localhost');
      if (url.pathname === '/__forge/jwks.json') {
        res.setHeader('Content-Type', 'application/json');
        res.setHeader('Access-Control-Allow-Origin', '*');
        res.setHeader('Cache-Control', 'public, max-age=3600');
        res.writeHead(200);
        res.end(JSON.stringify(sim.fit.getJWKS()));
        return;
      }
      next();
    };
    const viteStack = (viteServer.middlewares as any).stack;
    if (Array.isArray(viteStack)) {
      viteStack.unshift({ route: '', handle: forgeMiddleware });
    } else {
      viteServer.middlewares.use(forgeMiddleware);
    }

    console.log(`     ➜ Tools:   ${localUrl}__tools/`);
    console.log('');
    for (const mod of detectedModules) {
      console.log(`     ➜ ${mod.module.key}: ${localUrl}module/${mod.module.key}/`);
    }
    console.log('');
    console.log(`     🔧 Source maps enabled — debug in Chrome DevTools`);
    console.log(`     ♻️  HMR enabled — edits refresh automatically`);
    console.log('');
    console.log('  ─────────────────────────────────────────────');
    console.log('');

    // Open browser if requested
    if (open) {
      const { exec: execCmd } = await import('node:child_process');
      // Best-effort browser open — works on macOS, Linux, Windows
      const openCommand = process.platform === 'darwin'
        ? `open "${localUrl}"`
        : process.platform === 'win32'
          ? `start "${localUrl}"`
          : `xdg-open "${localUrl}" 2>/dev/null || true`;
      execCmd(openCommand);
    }

    // Graceful shutdown
    let cleaningUp = false;
    const cleanup = async () => {
      if (cleaningUp) return; // prevent double-fire
      cleaningUp = true;
      console.log('\n  🛑 Shutting down...');

      // Save state before teardown
      try {
        await saveState(sim, stateDir);
      } catch (err: any) {
        console.error(`  ⚠️  Failed to save state: ${err.message}`);
      }

      toolsServer.close();
      devServer.close();
      await viteServer.close();
      // Clean up temp dir
      try { rmSync(tempDir, { recursive: true, force: true }); } catch {}
      process.exit(0);
    };

    // Must prevent default SIGINT behavior so our async cleanup can finish
    process.on('SIGINT', () => { cleanup().catch(() => process.exit(1)); });
    process.on('SIGTERM', () => { cleanup().catch(() => process.exit(1)); });

  } catch (err: any) {
    console.error(`  ❌ Failed to start Vite: ${err.message}`);
    console.error('');
    console.error('  Make sure you have the required dependencies installed:');
    console.error('    npm install --save-dev vite @vitejs/plugin-react');
    console.error('');
    devServer.close();
    process.exit(1);
  }
}

